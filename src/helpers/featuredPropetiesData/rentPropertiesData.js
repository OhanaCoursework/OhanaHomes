export function comparePropertiesPriceDesc(a, b) {
  let priceA = parseInt(a.price.replaceAll(",", ""));
  let priceB = parseInt(b.price.replaceAll(",", ""));

  if (priceA < priceB) {
    return -1;
  }
  if (priceA > priceB) {
    return 1;
  }
  return 0;
}

